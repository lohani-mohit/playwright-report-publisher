#!/usr/bin/env node

const https = require("https");

const {
  GITHUB_TOKEN,
  REPORT_URL,
  TEST_STATUS,
  PULL_REQUEST_NUMBER,
  GITHUB_REPOSITORY,
  SUITE_NAME,
  TOTAL_TESTS,
  PASSED_TESTS,
  FAILED_TESTS,
  FLAKY_TESTS,
  SKIPPED_TESTS,
  DURATION,
} = process.env;

if (!PULL_REQUEST_NUMBER || !GITHUB_REPOSITORY) {
  console.log("Not a pull request or missing repo info — skipping PR comment");
  process.exit(0);
}

const failed = parseInt(FAILED_TESTS, 10) || 0;
const passed = parseInt(PASSED_TESTS, 10) || 0;
const flaky = parseInt(FLAKY_TESTS, 10) || 0;
const skipped = parseInt(SKIPPED_TESTS, 10) || 0;
const total = parseInt(TOTAL_TESTS, 10) || 0;
const duration = DURATION || "N/A";
const suiteName = SUITE_NAME || "Playwright";
const statusEmoji = TEST_STATUS === "passed" ? "✅" : "❌";

const REQUEST_TIMEOUT_MS = 30000;

let commentBody = `## ${statusEmoji} ${suiteName} Test Results\n\n`;

if (total > 0) {
  commentBody += `| Metric | Value |\n`;
  commentBody += `|--------|-------|\n`;
  commentBody += `| Total Tests | ${total} |\n`;
  commentBody += `| Passed ✅ | ${passed} |\n`;
  commentBody += `| Failed ❌ | ${failed} |\n`;
  if (flaky > 0) {
    commentBody += `| Flaky ⚠️ | ${flaky} |\n`;
  }
  if (skipped > 0) {
    commentBody += `| Skipped ⏭️ | ${skipped} |\n`;
  }
  commentBody += `| Duration | ${duration}s |\n\n`;
} else {
  commentBody += `**Status:** ${TEST_STATUS.toUpperCase()}\n\n`;
}

if (REPORT_URL) {
  commentBody += `📊 [View Full Report](${REPORT_URL})\n\n`;
}

commentBody += `---\n*Generated by [Playwright Report Publisher](https://github.com/lohani-mohit/playwright-report-publisher)*`;

const COMMENT_MARKER = `<!-- playwright-report-publisher: ${suiteName} -->`;
commentBody = `${COMMENT_MARKER}\n${commentBody}`;

const [owner, repo] = GITHUB_REPOSITORY.split("/");

function makeRequest(method, path, body) {
  return new Promise((resolve, reject) => {
    const data = body ? JSON.stringify(body) : "";
    const options = {
      hostname: "api.github.com",
      port: 443,
      path,
      method,
      headers: {
        "Content-Type": "application/json",
        Authorization: `token ${GITHUB_TOKEN}`,
        "User-Agent": "Playwright-Report-Publisher",
        ...(body ? { "Content-Length": Buffer.byteLength(data) } : {}),
      },
    };

    const req = https.request(options, (res) => {
      let responseData = "";
      res.on("data", (chunk) => (responseData += chunk));
      res.on("end", () => {
        const parsed = {
          statusCode: res.statusCode,
          headers: res.headers,
          body: JSON.parse(responseData || "{}"),
        };
        if (res.statusCode >= 400) {
          reject(
            new Error(`GitHub API ${res.statusCode}: ${responseData}`),
          );
        } else {
          resolve(parsed);
        }
      });
    });

    req.setTimeout(REQUEST_TIMEOUT_MS, () => {
      req.destroy(new Error(`Request timed out after ${REQUEST_TIMEOUT_MS}ms`));
    });

    req.on("error", reject);
    if (body) req.write(data);
    req.end();
  });
}

async function fetchAllComments() {
  const comments = [];
  let page = 1;

  while (true) {
    const res = await makeRequest(
      "GET",
      `/repos/${owner}/${repo}/issues/${PULL_REQUEST_NUMBER}/comments?per_page=100&page=${page}`,
    );
    comments.push(...(Array.isArray(res.body) ? res.body : []));

    const link = res.headers.link || "";
    if (!link.includes('rel="next"')) break;
    page++;
  }

  return comments;
}

async function main() {
  try {
    const comments = await fetchAllComments();

    const existing = comments.find(
      (c) => c.body && c.body.includes(COMMENT_MARKER),
    );

    if (existing) {
      await makeRequest(
        "PATCH",
        `/repos/${owner}/${repo}/issues/comments/${existing.id}`,
        { body: commentBody },
      );
      console.log(`Updated existing PR comment (id: ${existing.id})`);
    } else {
      await makeRequest(
        "POST",
        `/repos/${owner}/${repo}/issues/${PULL_REQUEST_NUMBER}/comments`,
        { body: commentBody },
      );
      console.log("Created new PR comment");
    }
  } catch (error) {
    console.error("Error posting GitHub PR comment:", error.message);
    process.exit(1);
  }
}

main();
